    public TableSet(final Collection<? extends C> c, final Collection<? extends R> r, final CrossHash.IHasher columnHasher, final CrossHash.IHasher rowHasher) {
        this(c, r, DEFAULT_LOAD_FACTOR, columnHasher, rowHasher);
    }

